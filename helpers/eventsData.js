// Helper functions to filter and format data
export const getEventsByCategory = (category) => {
  return masterEventData.filter(event => event.category === category)
}

export const getEventsBySubCategory = (subCategory) => {
  return masterEventData.filter(event => event.subCategory === subCategory)
}

export const getEventsByDay = (day) => {
  return masterEventData.filter(event => event.schedule[day] !== null)
}

export const getCompetitions = () => {
  return masterEventData.filter(event => event.category === 'Competition')
}

export const getGeneralEvents = () => {
  return masterEventData.filter(event => event.category === 'General')
}

export const getTalks = () => {
  return masterEventData.filter(event => event.category === 'Talk')
}

export const getWorkshops = () => {
  return masterEventData.filter(event => event.category === 'Workshop')
}

export const getConferences = () => {
  return masterEventData.filter(event => event.category === 'Conference')
}

// Format event for schedule display
export const formatEventForSchedule = (event, day) => {
  return {
    time: event.schedule[day],
    event: event.title.toUpperCase(),
    venue: event.venue,
    category: event.category
  }
}

// Format event for competitions display
export const formatEventForCompetition = (event) => {
  const day = Object.keys(event.schedule).find(day => event.schedule[day] !== null)
  return {
    title: event.title,
    category: event.subCategory,
    image: event.image,
    time: event.schedule[day]?.split(' TO ')[0] || 'TBA',
    date: day === 'Day 1' ? 'Jan 24' : day === 'Day 2' ? 'Jan 25' : 'Jan 26',
    prize: event.prize
  }
}